/**
 * @file config.h
 * @brief 机械AI仿生花引脚配置文件
 * 
 * 本文件定义了机械AI仿生花项目的所有GPIO引脚配置
 * 硬件平台：ESP32-C3
 * 
 * 【引脚分配表】
 * ┌─────────────┬──────────┬──────────┬────────────────────────────┐
 * │ 模块        │ ESP32引脚 │ 模块引脚  │ 说明                       │
 * ├─────────────┼──────────┼──────────┼────────────────────────────┤
 * │ INMP441麦克风│ GPIO3    │ BCLK/SCK │ I2S位时钟                  │
 * │ INMP441麦克风│ GPIO2    │ LRCLK/WS │ I2S左右声道时钟            │
 * │ INMP441麦克风│ GPIO10   │ DIN/SD   │ I2S数据输入                │
 * │ INMP441麦克风│ 3.3V     │ VDD      │ 电源正极                   │
 * │ INMP441麦克风│ GND      │ GND      │ 地线                       │
 * │ INMP441麦克风│ GND      │ L/R      │ 左右声道选择（接地=左声道）│
 * ├─────────────┼──────────┼──────────┼────────────────────────────┤
 * │ MAX98357A功放│ GPIO3    │ BCLK     │ I2S位时钟（与麦克风共用）  │
 * │ MAX98357A功放│ GPIO2    │ LRC      │ I2S左右声道时钟（共用）    │
 * │ MAX98357A功放│ GPIO13   │ DIN      │ I2S数据输出                │
 * │ MAX98357A功放│ 5V       │ VIN      │ 电源正极                   │
 * │ MAX98357A功放│ GND      │ GND      │ 地线                       │
 * ├─────────────┼──────────┼──────────┼────────────────────────────┤
 * │ SG90舵机    │ GPIO9    │ 信号线    │ PWM控制 (50Hz)             │
 * │ SG90舵机    │ 5V       │ VCC      │ 电源正极                   │
 * │ SG90舵机    │ GND      │ GND      │ 地线                       │
 * ├─────────────┼──────────┼──────────┼────────────────────────────┤
 * │ WS2812灯带  │ GPIO19   │ DIN      │ 数据输入                   │
 * │ WS2812灯带  │ 5V       │ VCC      │ 电源正极                   │
 * │ WS2812灯带  │ GND      │ GND      │ 地线                       │
 * ├─────────────┼──────────┼──────────┼────────────────────────────┤
 * │ 按钮1       │ GPIO0    │ 引脚1    │ 单击联网，长按开启蓝牙     │
 * │ 按钮1       │ GND      │ 引脚2    │ 地线                       │
 * │ 按钮2       │ GPIO1    │ 引脚1    │ 单击打断对话               │
 * │ 按钮2       │ GND      │ 引脚2    │ 地线                       │
 * │ 按钮3       │ GPIO12   │ 引脚1    │ 单击浇水，长按查询浇水时间 │
 * │ 按钮3       │ GND      │ 引脚2    │ 地线                       │
 * └─────────────┴──────────┴──────────┴────────────────────────────┘
 * 
 * @note 所有模块的GND必须连接在一起（共地）
 * @note INMP441必须接3.3V，不可接5V
 * @note GPIO引脚输出电压为3.3V，输入耐压也为3.3V
 */

#ifndef _BOARD_CONFIG_H_
#define _BOARD_CONFIG_H_

#include <driver/gpio.h>

/* ============================================================================
 * 音频配置
 * ============================================================================ */

/**
 * @brief 音频采样率
 * ESP32-C3全双工模式下TX和RX必须使用相同采样率
 * 使用16kHz统一采样率
 */
#define AUDIO_INPUT_SAMPLE_RATE  16000
#define AUDIO_OUTPUT_SAMPLE_RATE 16000

/* ============================================================================
 * INMP441 I2S麦克风引脚配置
 * ============================================================================ */

/**
 * @brief I2S位时钟引脚 (BCLK/SCK)
 * 连接INMP441的SCK引脚，与MAX98357A共用
 */
#define AUDIO_I2S_MIC_GPIO_SCK   GPIO_NUM_3

/**
 * @brief I2S左右声道时钟引脚 (LRCLK/WS)
 * 连接INMP441的WS引脚，与MAX98357A共用
 */
#define AUDIO_I2S_MIC_GPIO_WS    GPIO_NUM_2

/**
 * @brief I2S数据输入引脚 (DIN/SD)
 * 连接INMP441的SD引脚
 */
#define AUDIO_I2S_MIC_GPIO_DIN   GPIO_NUM_10

/* ============================================================================
 * MAX98357A I2S功放引脚配置
 * 
 * MAX98357A是I2S数字输入功放，与麦克风共用BCLK和WS引脚
 * 接线：
 *   MAX98357A BCLK → GPIO3（与麦克风共用）
 *   MAX98357A LRC  → GPIO2（与麦克风共用）
 *   MAX98357A DIN  → GPIO13
 *   MAX98357A VIN  → 5V
 *   MAX98357A GND  → GND
 * ============================================================================ */

/**
 * @brief MAX98357A功放数据输出引脚
 */
#define AUDIO_I2S_AMP_GPIO_DOUT  GPIO_NUM_13

/* ============================================================================
 * SG90舵机配置
 * ============================================================================ */

/**
 * @brief SG90舵机PWM控制引脚
 * 
 * PWM参数：
 * - 频率：50Hz (周期20ms)
 * - 脉宽范围：500μs ~ 2500μs 对应 0° ~ 180°
 * 
 * 注意：GPIO9在ESP32-C3上连接内部Flash，但仍可用于PWM输出
 */
#define SERVO_GPIO               GPIO_NUM_9

/* ============================================================================
 * WS2812灯带配置
 * ============================================================================ */

/**
 * @brief WS2812 RGB灯带数据输入引脚
 * 
 * WS2812时序要求严格，通常使用SPI+DMA方式驱动
 * 颜色格式：GRB (绿-红-蓝)
 */
#define WS2812_GPIO              GPIO_NUM_19

/* ============================================================================
 * 按钮配置
 * ============================================================================ */

/**
 * @brief 按钮1引脚 - 联网/蓝牙
 * GPIO0（开发板上的BOOT按钮）
 * - 单击：进入WiFi配网模式
 * - 长按：开启蓝牙配网
 */
#define BUTTON1_GPIO             GPIO_NUM_0

/**
 * @brief 按钮2引脚 - 打断对话
 * GPIO1
 * - 单击：打断当前对话，花朵合拢
 */
#define BUTTON2_GPIO             GPIO_NUM_1

/**
 * @brief 按钮3引脚 - 互动功能
 * GPIO12（输入专用引脚，但按钮为输入，可正常使用）
 * - 单击：浇水功能
 * - 长按：查询上次浇水时间
 */
#define BUTTON3_GPIO             GPIO_NUM_12

/* ============================================================================
 * LED配置
 * ============================================================================ */

/**
 * @brief 内置LED引脚
 * ESP32-C3开发板通常在GPIO8有内置LED
 */
#define BUILTIN_LED_GPIO         GPIO_NUM_8

/**
 * @brief BOOT按钮引脚（兼容旧代码）
 * 使用BUTTON1_GPIO作为BOOT按钮
 */
#define BOOT_BUTTON_GPIO         BUTTON1_GPIO

/* ============================================================================
 * 显示屏配置（本项目无显示屏）
 * ============================================================================ */

// 本项目没有物理显示屏
// 使用NoDisplay占位，所有显示相关功能被忽略

/* ============================================================================
 * 舵机角度配置
 * 
 * 舵机方向定义：
 * - 角度增大 = 顺时针旋转 = 开花（花瓣展开）
 * - 角度减小 = 逆时针旋转 = 合拢（花瓣收起）
 * 
 * 所以：
 * - 合拢位置 = 0°（逆时针最小角度，用户手动调节到此位置开机）
 * - 开花位置 = 48°（顺时针旋转48°，花瓣展开）
 * 
 * 开机默认：合拢状态（0°，用户手动调节）
 * 进入对话：开花状态（48°，顺时针旋转）
 * ============================================================================ */

/**
 * @brief 舵机合拢角度
 * 逆时针最小角度为合拢状态（花瓣收起）
 */
#define SERVO_CLOSE_ANGLE        0

/**
 * @brief 舵机开花角度
 * 顺时针旋转到48°为开花状态（花瓣展开）
 */
#define SERVO_OPEN_ANGLE         48

#endif // _BOARD_CONFIG_H_